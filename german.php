				<p class="para">In this free online Diploma in Basic German Language Studies course you will learn the basics of conversational German so that you will have the confidence to talk to and listen to native German speakers in a wide variety of situations. With 120 million German speakers in over 20 countries this course lays down the foundations of the spoken language by introducing learners to the pronunciation of words and the use of phrases and sentences through text and audio. The course begins by introducing you to the alphabet, numbers and words and the basics of conversation covering hello, goodbye and introductions. Audio segments will let you hear the sound of each letter in German, as well as how the various letters are pronounced in words. You will then learn the German words and phrases for telling the time and dates. You will also be introduced to nationalities, how to tell someone where you are from and ask where they are from. You will then learn German words and phrases that will help you order drinks and snacks at a cafe, how to reserve a table at a restaurant, how to order food and ask for and pay the bill. Next, the course introduces you to key words and phrases that allow you describe your family and talk about members of your family. The course then tells you how to describe your profession, where you work and aspects of your job. You will learn key words and phrases for shopping for day to day basics like bread, cheese, fruit and vegetables. When buying clothes you will learn how to ask for recommendations or assistance in a shop, and ask for an item of clothing by colour. If you are ill you will learn how to describe your symptoms to a pharmacist and request medicine. The remaining modules address leisure time such as sporting activities, going to the theater, and making plans for the weekend. You will also learn how to book a hotel or hostel, check in when you arrive, report any problems with your room, make inquires about activities, ask for information and directions as well as talk about your holidays to other people. This free online Diploma in Basic German Language Studies course will be of great interest to anybody wanting to travel to Germany, either as a tourist or in a professional capacity, business people who have regular contact with German colleagues and for any learner who has an interest in speaking or reading German..</p>
